"""You can include various mathematical comparisons in your conditional 
statements as well, such as less than, less than or equal to, greater than, 
and greater than or equal to."""

age = 18
print("\nIs age == 18? I predict True.")
print(age == 18)

print("\nIs age < 21? I predict True.")
print(age < 21)

print("\nIs age <= 21? I predict True.")
print(age <= 21)

print("\nIs age > 21? I predict False.")
print(age > 21)

print("\nIs age >= 21? I predict False.")
print(age >= 21)